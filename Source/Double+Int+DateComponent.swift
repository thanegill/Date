import Foundation

extension Double {
    public var Nanoseconds: Time.Nanoseconds {
        return Time.Nanoseconds(self)
    }
    public var Microseconds: Time.Microseconds {
        return Time.Microseconds(self)
    }
    public var Milliseconds: Time.Milliseconds {
        return Time.Milliseconds(self)
    }
    public var Seconds: Time.Seconds {
        return Time.Seconds(self)
    }
    public var Minutes: Time.Minutes {
        return Time.Minutes(self)
    }
    public var Hours: Time.Hours {
        return Time.Hours(self)
    }
    public var Days: Time.Days {
        return Time.Days(self)
    }
    public var Weeks: Time.Weeks {
        return Time.Weeks(self)
    }
}

